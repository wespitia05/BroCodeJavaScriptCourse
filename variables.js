// variables = a container that stores a value
//             behaves as if it were the value it contains

// 1. declaration   let x;
// 2. assignment    x = 100;

// declare/assign our values
let age = 25;
let price = 10.99;
let gpa = 2.1;

// print those values in the console
console.log(age);
console.log(price);
console.log(gpa);

// print sentences with our values in the console
console.log(`you are ${age} years old`);
console.log(`the price is $${price}`);
console.log(`your gpa is: ${gpa}/4.0`)

// print the type of object each value is
console.log(typeof age);
console.log(typeof price);
console.log(typeof gpa);